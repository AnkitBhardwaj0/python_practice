#Take two numbers as input and print their:
"""
Sum
Difference
Product
Division
Floor division
Modulus
Power
"""
a=int(input("enter a number"))
b=int(input("enter a number"))
sum=a+b
print(f"sum of {a} and {b} is {sum}")
difference=a-b
print(f"difference of {a} and {b} is {difference}")
product=a*b
print(f"product of {a} and {b} is {product}")
division=a/b 
print(f"division of {a} and {b} is {division}")
floor_div=a//b
print(f"floor division of {a} and {b} is {floor_div}")
modulus=a%b
print(f"modulus of {a} and {b} is {modulus}")
power=a**b
print(f"power of {a} to the power {b} is {power}")
